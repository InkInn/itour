package com.itour.api.model;

public class Attraction {
	
	private String name;
	
	private String attCoode;
	
	public String introduc;
	
	public String imgUrl;
	
	public String cityCode;
	
	public String proCode;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


	public String getAttCoode() {
		return attCoode;
	}

	public void setAttCoode(String attCoode) {
		this.attCoode = attCoode;
	}

	public String getIntroduc() {
		return introduc;
	}

	public void setIntroduc(String introduc) {
		this.introduc = introduc;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

	public String getCityCode() {
		return cityCode;
	}

	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}

	public String getProCode() {
		return proCode;
	}

	public void setProCode(String proCode) {
		this.proCode = proCode;
	}
	
	
}
